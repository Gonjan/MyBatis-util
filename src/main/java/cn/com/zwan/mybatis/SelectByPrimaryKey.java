package cn.com.zwan.mybatis;

public class SelectByPrimaryKey {
	private String parameterType;
	private String tableName;
	private String colPk;
	private String javaPk;
	
	
	public String getParameterType() {
		return parameterType;
	}
	public void setParameterType(String parameterType) {
		this.parameterType = parameterType;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getColPk() {
		return colPk;
	}
	public void setColPk(String colPk) {
		this.colPk = colPk;
	}
	public String getJavaPk() {
		return javaPk;
	}
	public void setJavaPk(String javaPk) {
		this.javaPk = javaPk;
	}
	
}
